// Basic Math with placeholders
// Rust has a built-in math library that can be used to perform basic math operations.
// We can perform basic math and the placeholder gets replaced with the result.

fn main() {
    println!("{} + {} = {}", 10, 10, 10 + 10); // 10 is the first placeholder, 10 is the second placeholder and 20 is the result.
}

// output: 10 + 10 = 20
