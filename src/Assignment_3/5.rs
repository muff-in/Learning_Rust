// Placeholder Traits
// {:b},{:x},{:o} ... {:b} is binary, {:x} is hexadecimal, {:o} is octal.

// in this program we are converting the value of 10 to binary, hexadecimal, and octal:
fn main() {
    println!(
        "Number : 10 \nBinary:{:b} Hexadecimal:{:x} Octal:{:o}",
        10, 10, 10
    ); // \n is used to print on new line.
}

// output: Number : 10
// Binary:1010 Hexadecimal:a Hexadecimal:a Octal:12
