
// Size of an Array

// To access the length of the array, use the built-in function len.

fn main() {
    
    let arr:[i32;4] = [1, 2, 3, 4];  //define an array of size 4
   
    println!("Length of array: {}", arr.len());  // to print the length of array we use array_name.len()
}

// output: Length of array: 4