
// Print the Array

// The whole array can be traversed using a loop or the debug trait.

fn main() {
    
    let arr:[i32;4] = [1, 2, 3, 4]; //define an array of size 4
    
    println!("\nPrint using a debug trait");
    println!("Array: {:?}", arr); //Using debug trait
}

// output: Print using a debug trait
// Array: [1, 2, 3, 4]