// Assignment and Compound Assignment Operators

// Assignment Operator

// The assignment operator is used to save a value in the variable.
// Rust has only one assignment operator, = . The following table defines the function of the operator.

fn main() {
    let a = 2; // a is assigned the value 2
    let b = a; // b is assigned the value of a
    println!("b = a"); // print b = a
    println!("Value of a:{}", a); //  print the value of a
    println!("Value of b:{}", b); // print the value of b
}

// output: b = a
// Value of a:2
// Value of b:2
