// Arithmetic operators are used to perform arithmetic operations.
// Operand - the quantity on which an operation is to be done.

fn main() {
    let a = 4; // define a variable
    let b = 3;

    println!("Operand 1:{}, Operand 2:{}", a, b); // print the operands

    println!("Addition:{}", a + b); // print the addition of the operands

    println!("Subtraction:{}", a - b); // print the subtraction of the operands

    println!("Multiplication:{}", a * b); // print the multiplication of the operands

    println!("Division:{}", a / b); // print the division of the operands

    println!("Modulus:{}", a % b); // print the modulus of the operands
}

// output: Operand 1:4, Operand 2:3
// Addition:7
// Subtraction:1
// Multiplication:12
// Division:1
// Modulus:1
