// Comparison Operators

// Comparison Operators are used for comparing the values of two operands.

fn main() {
    let a = 2;
    let b = 3;
    println!("Operand 1:{}, Operand 2:{}", a, b); // print the operands
    println!("a > b:{}", a > b); // print the greater than of the operands
    println!("a < b:{}", a < b); // print the less than of the operands
    println!("a >= b:{}", a >= b); // print the greater than or equal to of the operands
    println!("a <= b:{}", a <= b); // print the less than or equal to of the operands
    println!("a == b:{}", a == b); // print the equal to of the operands
    println!("a != b:{}", a != b); // print the not equal to of the operands
}

// output: Operand 1:2, Operand 2:3
// a > b:false
// a < b:true
// a >= b:false
// a <= b:true
// a == b:false
// a != b:true
