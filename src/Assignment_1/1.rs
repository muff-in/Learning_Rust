// fn - function notation. function starts with the keyword fn and then the name of the function.
// main() - main function is the beginning of every Rust program.
// In this program we declares a function named main that takes no arguments and returns nothing.
// If arguments were present they would have been passed within the round brackets ( ).

fn main() {
    // The function body starts with the opening curly brace {

    println!("Hello World!"); // println! is a macro that prints to the console.
                              // string to be printed is passed in the double quote.

    // line ends with a semicolon which indicates that the expression is over and the next one is ready to begin.
} // function body ends with a closing curly brace }

// output: Hello World!
